class Operators{
    public static void main(String[] args){
	
        int a = 64;
        int b = 7;
        float c = 4.3f;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a/b);
        System.out.println(a%b);
        System.out.println(a%c);
    }
}